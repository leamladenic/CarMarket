    public void fillJtableVehicles(){
        
        V_VEHICLE vehicle = new V_VEHICLE();
        ArrayList<V_VEHICLE> vehicleList = vehicle.vehiclesList();
        
        String[] colNames = {"ID",  "Owner ID", "Price", "Brand", "Year"};
        
        Object[][] rows = new Object[vehicleList.size()][5];
        
        for(int i = 0; i < vehicleList.size(); i++){
            rows[i][0] = vehicleList.get(i).getId();
            rows[i][1] = vehicleList.get(i).getOwnerId();
            rows[i][2] = vehicleList.get(i).getPrice();
            rows[i][3] = vehicleList.get(i).getBrand();
            rows[i][4] = vehicleList.get(i).getYear();
        }
        
        DefaultTableModel model = (new DefaultTableModel(rows, colNames));
        jTable_Vehicles.setModel(model);
      
        jTable_Vehicles.setRowHeight(40);
        jTable_Vehicles.setSelectionBackground(new Color(0,153,76));
        jTable_Vehicles.getColumnModel().getColumn(0).setPreferredWidth(25);
        
        
    }

