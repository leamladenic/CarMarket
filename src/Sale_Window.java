    private void jButton_Remove_SaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Remove_SaleActionPerformed

       try{
            int id = Integer.valueOf(jTextField_Id.getText());
       
            if(new V_SALE().deleteSale(id)){
                JOptionPane.showMessageDialog(null, "Sale Data Has Been Deleted", "Delete Sale", 0);
            }else {
                JOptionPane.showMessageDialog(null, "Sale Data Has NOT Been Deleted", "Delete Sale", 0);
            }
       }catch (Exception ex){
           JOptionPane.showMessageDialog(null, "Select The Sale ID", "Delete Sale ERROR", 0);
       }
        
       
        
    }//GEN-LAST:event_jButton_Remove_SaleActionPerformed

